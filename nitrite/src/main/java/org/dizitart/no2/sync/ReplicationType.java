package org.dizitart.no2.sync;

/**
 * Represents different types of replication.
 *
 * @author Anindya Chatterjee
 * @since 1.0
 */
public enum ReplicationType {
    /**
     * Pull replication type.
     */
    PULL,
    /**
     * Push replication type.
     */
    PUSH,
    /**
     * Both way replication type.
     */
    BOTH_WAY
}
